using ChristianHelle.Framework.WindowsMobile.Forms;

namespace ChristianHelle.Framework.WindowsMobile.Forms
{
    /// <summary>
    /// <see cref="ButtonEx"/> Color theme
    /// </summary>
    public enum ButtonTheme
    {
        /// <summary>
        /// Default color theme
        /// </summary>
        /// <remarks>Draws a gradient fill using the start/end shades defined in the control</remarks>
        Default,
        /// <summary>
        /// Black color theme
        /// </summary>
        /// <remarks>Draws a gradient fill with 2 shades of black to the control</remarks>
        Black,
        /// <summary>
        /// Red color theme
        /// </summary>
        /// <remarks>Draws a gradient fill with 2 shades of red to the control</remarks>
        Red,
        /// <summary>
        /// Green color theme
        /// </summary>
        /// <remarks>Draws a gradient fill with 2 shades of green to the control</remarks>
        Green,
        /// <summary>
        /// Blue color theme
        /// </summary>
        /// <remarks>Draws a gradient fill with 2 shades of blue to the control</remarks>
        Blue        
    }
}